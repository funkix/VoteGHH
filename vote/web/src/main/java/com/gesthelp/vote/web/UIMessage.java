package com.gesthelp.vote.web;

public class UIMessage {

	private String message;
	private UIMessageType messageTypeRef;

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public UIMessageType getMessageTypeRef() {
		return messageTypeRef;
	}

	public void setMessageTypeRef(UIMessageType messageTypeRef) {
		this.messageTypeRef = messageTypeRef;
	}

}
